# -*- coding: utf-8 -*-

# このプログラムは組み込み関数だけで作られています。

# GoogleStyle非準拠ポイント(1): モジュールレベルのdocstringの欠如
# 本来、ファイルの先頭には"""で囲まれたモジュールの説明（docstring）を
# 記述することが推奨されますが、ここでは通常のコメントで代用しています。

# GoogleStyle非準拠ポイント(2): mainガードの欠如
# スクリプトとして実行されるコードは、if __name__ == '__main__':ブロックで
# 囲むことが推奨されますが、このコードはトップレベル（グローバルスコープ）で
# 直接処理を開始しています。

print('*** 簡単なプロフィール作成プログラム ***')
print('あなたの名前と年齢を教えてください。')
print('----------------------------------')

# input()関数を使い、ユーザーからのキーボード入力を受け取ります。
name = input('名前を入力してください > ')

# 無限ループを開始します。
while True:
  # GoogleStyle非準拠ポイント(3): インデントのスタイル
  # インデントにスペース2つを使用しています。GoogleStyleでは
  # スペース4つを使用することが規約です。
  age_input = input('年齢を数字で入力してください > ')

  try:
    # int()関数を使って、入力された文字列を整数（数値）に変換します。
    age = int(age_input)
    break
  except ValueError:
    print('（エラー）数字以外が入力されました。もう一度入力してください。')

# GoogleStyle非準拠ポイント(4): 変数名の命名規則
# 変数名に「キャメルケース（nameLength）」を使用しています。
# GoogleStyleやPEP8では、「スネークケース（name_length）」が推奨されます。
nameLength = len(name)

# GoogleStyle非準拠ポイント(5): 演算子の前後のスペース
# 足し算の演算子「+」の両側にスペースを入れていません。
# GoogleStyleでは、二項演算子の両側にはスペースを1つずつ入れることが推奨されます。(例: age + 10)
#
# さらに、この変数名(FutureAge)はクラス名で使われる「パスカルケース」のため、
# 通常の変数名としては不適切です。
FutureAge = age+10

# GoogleStyle非準-拠ポイント(6): 型ヒントの欠如
# GoogleStyleでは、変数や関数の引数、戻り値に型ヒント（Type Hints）を
# 付けることが推奨されています。(例: name: str, age: int)
# このプログラムでは、型ヒントを一切使用していません。

print('----------------------------------')
print(f'こんにちは、{name}さん！')
# 変数名を変更したため、f文字列の中も合わせます。
print(f'あなたの名前は{nameLength}文字ですね。')
print(f'10年後には{FutureAge}歳になっています。')

print('\n--- データ型の確認 ---')
print(f'変数「name」の型は {type(name)} です。')
print(f'変数「age」の型は {type(age)} です。')
